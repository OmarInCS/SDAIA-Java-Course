package day1.session2;

public class NestedLoops {
    public static void main(String[] args) {

        for (int y = 1; y <= 10; y++) {
            for (int x = 1; x <= 10; x++) {
                System.out.print(x + "\t");
            }
            System.out.println();
        }

        System.out.println("--------------");
    }
}
